    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($slug)
    {
        $singleGame = Http::withHeaders(config('services.igdb'))
        ->withBody(
            "fields name, cover.url, first_release_date, platforms.abbreviation, rating, summary, videos.video_id, screenshots.url, aggregated_rating;
            where slug = \"{$slug}\";",
            "text/plain"
        )
        ->post('https://api.igdb.com/v4/games/')
        ->json();

        abort_if(!$singleGame, 404);

        return view('show', compact('singleGame'));
    }

